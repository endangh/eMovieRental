    private void initFinals() {
        textDenda.setValue(new Integer(prop.getProperty("DENDA")));
        textHarga.setValue(new Integer(prop.getProperty("HARGA")));
        textMaksimal.setValue(new Integer(prop.getProperty("MAKSIMAL")));
    }

    private void initVariables() {
        prop = new Properties();
        try {
            prop.loadFromXML(new FileInputStream("setting\\emovierental.xml"));
        } catch (IOException ex) {
            prop.setProperty("MAKSIMAL", "5");
            prop.setProperty("DENDA", "1000");
            prop.setProperty("HARGA", "1000");
        }
    }

