    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        try {
            prop.setProperty("MAKSIMAL", textMaksimal.getValue().toString());
            prop.setProperty("DENDA", textDenda.getValue().toString());
            prop.setProperty("HARGA", textHarga.getValue().toString());
            prop.storeToXML(new FileOutputStream("setting\\emovierental.xml"), "e'MovieRental");//GEN-LAST:event_buttonSimpanActionPerformed
            JOptionPane.showMessageDialog(this, "pengaturan berhasil disimpan");
        } catch (IOException ex) {
            WidgetUtilities.showErrorMessage(this, ex.getMessage());
        }
    }
